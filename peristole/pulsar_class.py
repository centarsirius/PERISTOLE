class pulsar:

	# The init method or constructor
	def __init__(self, mass):
		self.mass=mass
	
	# Adds an instance variable
	def axis(self, axis):
		self.axis = axis

	def e(self, ecc):
		self.ecc = ecc

	def omega(self, omega):
		self.omega = omega

	def angle(self, angle):
		self.angle = angle

	def spin(self, spin):
		self.spin = spin

	def eta(self, eta):
		self.eta = eta

	def zeta(self, zeta):
		self.zeta = zeta
    
	def alpha(self, alpha):
		self.alpha = alpha

	def big_phi0(self, big_phi0):
		self.big_phi0 = big_phi0